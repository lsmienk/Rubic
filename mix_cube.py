def f_mix(arg, g, b, r, o, w):
    if arg == "F2":
        g[0], g[1], g[2], g[3], g[5], g[6], g[7], g[8] = g[8], g[7], g[6], g[5], g[3], g[2], g[1], g[0]
        i = 6
    elif arg == "F'":
        i = 3
        g[0], g[1], g[2], g[3], g[5], g[6], g[7], g[8] = g[6], g[3], g[0], g[7], g[1], g[8], g[5], g[2]
    else:
        i = -3
        g[0], g[1], g[2], g[3], g[5], g[6], g[7], g[8] = g[2], g[5], g[8], g[1], g[7], g[0], g[3], g[6]
    arr = [w[6], w[7], w[8], r[0], r[3], r[6], b[2], b[1], b[0], o[8], o[5], o[2]]
    arr = arr[-i:] + arr[:-i]
    w[6], w[7], w[8] = arr[0 : 3]
    r[0], r[3], r[6] = arr[3 : 6]
    b[2], b[1], b[0] = arr[6 : 9]
    o[8], o[5], o[2] = arr[9 : 12]
    
